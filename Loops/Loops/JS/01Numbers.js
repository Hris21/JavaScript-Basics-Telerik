//Write a script that prints all the numbers from 1 to N.
console.log("Task 1");
console.log("\n");
function numbers(N) {
    var i;
    for (i = 1; i <= N; i++) {
        console.log(i);
    }
}
numbers(5);
console.log('<--------------.------------>');
numbers(17);
console.log('<--------------.------------>');
numbers(3);